BEGIN
  FOR i IN 1..10000 LOOP -- Cambia 10000 por la cantidad de filas que quieres generar.
    INSERT INTO alojamientos (
      activa, 
      checkin, 
      checkout, 
      acompanantes, 
      idplan, 
      idalojamiento
    ) VALUES (
      CASE MOD(i, 2) WHEN 0 THEN 'SI' ELSE 'NO' END, -- Alterna entre 'SI' y 'NO'.
      TO_DATE('2005-01-01', 'YYYY-MM-DD') + TRUNC(DBMS_RANDOM.VALUE(0, 365 * (EXTRACT(YEAR FROM SYSDATE) - 2005))), -- Genera una fecha aleatoria de checkin desde el año 2005 hasta la fecha actual.
      TO_DATE('2005-01-01', 'YYYY-MM-DD') + TRUNC(DBMS_RANDOM.VALUE(365 * (EXTRACT(YEAR FROM SYSDATE) - 2004), 365 * (EXTRACT(YEAR FROM SYSDATE) - 2003))), -- Genera una fecha de checkout posterior al checkin.
      TRUNC(DBMS_RANDOM.VALUE(0, 9)), -- Genera un número entero aleatorio de acompañantes entre 0 y 8.
      MOD(i, 200) + 1, -- Asigna un idplan de manera cíclica entre 1 y 200.
      i -- Asigna un idalojamiento incremental.
    );
    IF MOD(i, 1000) = 0 THEN -- Realiza un commit cada 1000 registros para evitar bloqueos y uso excesivo de memoria.
      COMMIT;
    END IF;
  END LOOP;
  COMMIT;
END;
